import { ITodoType } from "./interfaces";

export function filterTodos(todos: ITodoType[], tab: string) {
  return todos.filter(todo => {
    if (tab === 'all') {
      return true;
    } else if (tab === 'active') {
      return !todo.completed;
    } else if (tab === 'completed') {
      return todo.completed;
    }
  });
}
